<!-- Enhanced Carousel JavaScript -->
<script>
  document.addEventListener('DOMContentLoaded', function () {
    // Initialize all carousels
    const carousels = ['#tripsCarousel', '#umrahCarousel', '#holidayCarousel', '#uaeCarousel', '#newsCarousel'];

    carousels.forEach(carouselId => {
      const carouselElement = document.querySelector(carouselId);
      if (!carouselElement) return;

      const carousel = new bootstrap.Carousel(carouselElement, {
        interval: 2000, // 2 seconds
        ride: 'carousel',
        pause: 'hover',
        wrap: true,
        touch: true
      });

      // Add progress bar animation
      const progressBar = carouselElement.querySelector('.carousel-progress-bar');
      if (progressBar) {
        let progressInterval;

        function startProgress() {
          progressBar.style.width = '0%';
          progressBar.style.transition = 'width 2000ms linear';
          setTimeout(() => {
            progressBar.style.width = '100%';
          }, 50);
        }

        function resetProgress() {
          progressBar.style.transition = 'none';
          progressBar.style.width = '0%';
        }

        // Start progress on page load
        startProgress();

        // Reset and restart progress on slide change
        carouselElement.addEventListener('slide.bs.carousel', function () {
          resetProgress();
          setTimeout(startProgress, 100);
        });

        // Pause progress on hover
        carouselElement.addEventListener('mouseenter', function () {
          progressBar.style.animationPlayState = 'paused';
        });

        carouselElement.addEventListener('mouseleave', function () {
          progressBar.style.animationPlayState = 'running';
        });
      }

      // Enhanced drag functionality for desktop
      let isMouseDown = false;
      let startX = 0;
      let currentX = 0;
      let isDragging = false;

      carouselElement.addEventListener('mousedown', function (e) {
        isMouseDown = true;
        startX = e.clientX;
        carouselElement.classList.add('dragging');
        e.preventDefault();
      });

      carouselElement.addEventListener('mousemove', function (e) {
        if (!isMouseDown) return;

        currentX = e.clientX;
        const diffX = startX - currentX;

        if (Math.abs(diffX) > 10) {
          isDragging = true;
        }
      });

      carouselElement.addEventListener('mouseup', function (e) {
        if (isDragging) {
          const diffX = startX - currentX;

          if (Math.abs(diffX) > 50) { // Minimum drag distance
            if (diffX > 0) {
              carousel.next();
            } else {
              carousel.prev();
            }
          }
        }

        isMouseDown = false;
        isDragging = false;
        carouselElement.classList.remove('dragging');
      });

      carouselElement.addEventListener('mouseleave', function () {
        isMouseDown = false;
        isDragging = false;
        carouselElement.classList.remove('dragging');
      });

      // Enhanced touch functionality for mobile
      let touchStartX = 0;
      let touchEndX = 0;

      carouselElement.addEventListener('touchstart', function (e) {
        touchStartX = e.touches[0].clientX;
      }, { passive: true });

      carouselElement.addEventListener('touchend', function (e) {
        touchEndX = e.changedTouches[0].clientX;
        handleTouch();
      }, { passive: true });

      function handleTouch() {
        const touchDiff = touchStartX - touchEndX;

        if (Math.abs(touchDiff) > 50) { // Minimum swipe distance
          if (touchDiff > 0) {
            carousel.next();
          } else {
            carousel.prev();
          }
        }
      }

      // Keyboard navigation
      carouselElement.addEventListener('keydown', function (e) {
        if (e.key === 'ArrowLeft') {
          carousel.prev();
          e.preventDefault();
        } else if (e.key === 'ArrowRight') {
          carousel.next();
          e.preventDefault();
        }
      });

      // Make carousel focusable for keyboard navigation
      carouselElement.setAttribute('tabindex', '0');
    });
  });
</script>


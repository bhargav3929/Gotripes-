    /**
     * Attempt to split a stored phone string into [dial_code, national_number].
     * Returns ['', $phone] if no known dial code prefixes the value.
     * Longest-prefix match wins (e.g. "+1876" before "+1").
     */
    public static function split(?string $phone): array
    {
        $phone = trim((string) $phone);
        if ($phone === '') {
            return ['', ''];
        }

        $compact = preg_replace('/\s+/', '', $phone);

        $dials = array_column(self::all(), 'dial');
        usort($dials, fn($a, $b) => strlen($b) <=> strlen($a));

        foreach ($dials as $dial) {
            if (str_starts_with($compact, $dial)) {
                $rest = substr($compact, strlen($dial));
                return [$dial, ltrim($rest)];
            }
        }

        return ['', $phone];
    }
